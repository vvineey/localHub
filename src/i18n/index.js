const initialLocale = languageOptions.some((item) => item.value === storedLocale)
  ? storedLocale
  : languageOptions.some((item) => item.value === browserLocale)
    ? browserLocale
    : 'ko'

document.documentElement.lang = initialLocale

export const i18n = createI18n({
  legacy: false,
  globalInjection: true,
  locale: initialLocale,
  fallbackLocale: 'ko',
  messages,
})
